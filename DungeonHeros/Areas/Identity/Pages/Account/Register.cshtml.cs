            _manager = manager;
            
            foreach (var race in _dbContext.Races.ToList().Where(race => race.IsForUser != 0))
            {
                Races.Add(new SelectListItem(){Text = race.Name, Value = race.RaceId.ToString()});
            }

            Classes = new List<SelectListItem>();
            foreach (var classe in _dbContext.Classes.ToList())
            {
                Classes.Add(new SelectListItem{Text = classe.ClassName, Value = classe.ClassId.ToString()});
            }
